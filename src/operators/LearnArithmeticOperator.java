package operators;

public class LearnArithmeticOperator {

    // Arithmetic operator
    // (+)/ additive operator
    // (-) / subtraction operator
    // (*) / multiplication operator
    // (/) / division operator
    // (%) / remainder operator

    public static void main(String[] args) {
        int num1= 42;
        int num2=12;
        int sum=num1+num2;
        int sub=num1-num2;
        int multi= num1*num2;
        int div= num1/num2;
        int remainder= num1%num2;
        // call all this variable

        System.out.println("summation of two numbers is "+sum);
        System.out.println("subtraction of two numbers is "+sub);
        System.out.println("multiplication of two numbers is "+multi);
        System.out.println("division of two numbers is "+div);
        System.out.println("remainder of two numbers is "+remainder);
    }


}
